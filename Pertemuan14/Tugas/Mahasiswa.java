package AlgoritmaDanStrukturData.Pertemuan14.Tugas;

public class Mahasiswa {
    String nim;
    String nama;
    String jur;
    String prodi;

    public Mahasiswa(String nim, String nama, String jur, String prodi) {
        this.nim = nim;
        this.nama = nama;
        this.jur = jur;
        this.prodi = prodi;
    }

    public String getNim() {
        return nim;
    }    
}
